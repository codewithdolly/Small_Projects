//Light on
function lightOn() {
  document.getElementById("myImg").src = "light on.jpg";
  document.getElementById("myImg").setAttribute("width", "200");
  document.getElementById("myImg").setAttribute("height", "270");
}


//light off
function lightOff() {
  document.getElementById("myImg").src = "light off.jpg";
  document.getElementById("myImg").setAttribute("width", "290");
}
